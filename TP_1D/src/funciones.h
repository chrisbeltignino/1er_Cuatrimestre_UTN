/** \brief función para encapsular el menu de opciones.
*/
void menu ();

/** \brief funcion para pedir numero.
 *
 * \param char mensaje[] se escribe el mensaje
 * \return El numero ingresado
 */
float pedirNumero(char mensaje[]);

/** \brief Funcion de suma
 *
 * \param El primer numero ingresado
 * \param El segundo numero ingresado
 * \return El resultado de la suma de ambos numeros
 */
float suma(float numero1,float numero2);

/** \brief Funcion de resta
 *
 * \param El primer numero ingresado
 * \param El segundo numero ingresado
 * \return El resultado de la resta de ambos numeros
 */
float resta(float numero1,float numero2);

/** \brief Funcion de division
 *
 * \param El primer numero ingresado
 * \param El segundo numero ingresado
 * \return El resultado de la division de ambos numeros
 */
float division(float numero1,float numero2);

/** \brief Funcion de multiplicacion
 *
 * \param El primer numero ingresado
 * \param El segundo numero ingresado
 * \return El resultado de la multiplicacion de ambos numeros
 */
float multiplicacion(float numero1,float numero2);

/** \brief Funcion de factorial con recursividad
 *
 * \param El primer numero ingresado
 *
 * \return El resultado del factorial del numero ingresado.
 */
int resolverFactorial(int numero);






